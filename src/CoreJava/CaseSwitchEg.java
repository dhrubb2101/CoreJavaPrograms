package CoreJava;

public class CaseSwitchEg {

	public static void main(String[] args) {
		int number = 80;
		
		switch(number) {
		
		//case statements
		
		case 10 : System.out.println("The no is 10");
		
		break;
		
        case 20 : System.out.println("The no is 20");
		
		break;
		
        case 30 : System.out.println("The no. is 30");
        
        break;
        
        case 40 : System.out.println("The no. is 40");
        
        break;
        
        default: System.out.println("The value is invalid");
		}
		
	
	
	}

}
